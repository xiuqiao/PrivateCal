//判断系统是否是iOS7
+ (BOOL)isiOS7
{
    CGFloat version = [[UIDevice currentDevice].systemVersion floatValue];
    return version >= 7.0;
}

+(BOOL)isiOS8{
    CGFloat version = [[UIDevice currentDevice].systemVersion floatValue];
    
    return version >= 8.0;
}

//判断设备模型
+ (NSString *)deviceModel
{
    return [UIDevice currentDevice].model;
}

//获取屏幕高度,根据屏幕的状态
+ (CGFloat)screenHeight
{
    UIInterfaceOrientation orientation = [UIApplication sharedApplication].statusBarOrientation;
    //上是2,下是1，左是4，右是3,button的位置
    if(orientation == UIInterfaceOrientationLandscapeLeft || orientation == UIInterfaceOrientationLandscapeRight) {
        return [UIScreen mainScreen].bounds.size.width;
    } else {
        return [UIScreen mainScreen].bounds.size.height;
    }
}

//获取屏幕宽度
+ (CGFloat)screenWidth
{
    UIInterfaceOrientation orientation = [UIApplication sharedApplication].statusBarOrientation;
    if(orientation == UIInterfaceOrientationLandscapeLeft || orientation == UIInterfaceOrientationLandscapeRight){
        return [UIScreen mainScreen].bounds.size.height;
    } else {
        return [UIScreen mainScreen].bounds.size.width;
    }
}

//返回设备是否横屏
+ (BOOL)isLandscape
{
    UIInterfaceOrientation orientation = [UIApplication sharedApplication].statusBarOrientation;
    if(orientation == UIInterfaceOrientationLandscapeLeft || orientation == UIInterfaceOrientationLandscapeRight){
        return YES;
    } else {
        return NO;
    }
}

+ (void)changeScrollViewEdgeInset:(UIScrollView *)scrollView{
    
    if ([[[UIDevice currentDevice] systemVersion] floatValue] <= 6.00001f) {
        
        CGFloat topInset = scrollView.contentInset.top+49;
        CGFloat leftInset = scrollView.contentInset.left;
        CGFloat bottomInset = scrollView.contentInset.bottom;
        
        CGFloat rightInset = scrollView.contentInset.right;
        
        scrollView.contentInset = UIEdgeInsetsMake(topInset, leftInset, bottomInset, rightInset);
    }
}

